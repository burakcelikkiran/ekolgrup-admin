                                    <br>
                                    İslam coğrafyasındaki sorunları hastalıklara benzeten Tarhan, "Bunlardan biri ümitsizlik hastalığı, bizi en çok bloke eden hastalık. İkinci hastalık, savaşçı bir akılla hareket ediyoruz, oysa İslam'da barış esastır. Asıl cihat, manevi cihattır. Diğer bir hastalığımız da baskıcılığın, otoriterliğin hastalık şeklinde yayılması. Diğer bir hastalık da mehdi bekler gibi bir kurtarıcı beklenmesidir. 'Cemaat narsisizmi', yani kendi cemaatinin menfaatini, ümmetin menfaatinin üzerinde tutmak da ayrı bir hastalık." değerlendirmesini yaptı.<br>
