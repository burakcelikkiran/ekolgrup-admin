                                <p><br> Adaleti Savunanlar Stratejik Araştırmalar Merkezi Derneği (ASSAM) Yönetim Kurulu Başkanı ve Cumhurbaşkanı Başdanışmanı Adnan Tanrıverdi, üçüncü dünya savaşının İslam ülkelerine karşı ilan edilmemiş bir savaş olarak sürdürüldüğünü söyledi.<br>
                                    <br>
                                    Tanrıverdi, ASSAM, Adaleti Savunanlar Derneği (ASDER), Üsküdar Üniversitesi ve İslam Dünyası STK'ları Birliği tarafından, İslam ülkelerinde yaşanan güncel sorunlara akademik ve siyasal tespit ve çözüm önerileri üretmek amacıyla düzenlenen ve 35 İslam ülkesinden temsilcilerin katıldığı "1. Uluslararası ASSAM İslam Birliği Kongresi"nin açılışında yaptığı konuşmada, İslam dünyasının küresel güçlerin hedefi olduğunu belirtti.<br>
                                    <br>
                                    Osmanlı'nın yıkılmasından sonra yönetimsiz kalan Müslümanların küçük devletçikler kurarak varlıklarını devam ettirmeye çalıştığını ancak birlik ve beraberliklerini kaybettiklerinden küresel güçler için kolay lokma haline geldiklerini ifade eden Tanrıverdi, "Üçüncü dünya savaşı, İslam ülkelerine karşı ilan edilmemiş bir savaş olarak sürdürülmektedir. ABD, AB, Bağımsız Devletler Toplululuğu, İslam coğrafyasını ele geçirmek için İslam ülkelerinde kontrol ettikleri terör örgütleri vasıtasıyla vekalet savaşları yürüterek, otorite tesis etmeyi amaçlamaktadır." diye konuştu.</p>

